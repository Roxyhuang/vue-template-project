/* eslint-disable no-new */
class Console {
  static log(msg) {
    console.log(msg);
  }

  static error(msg) {
    console.error(msg);
  }

  static info(msg) {
    console.info(msg);
  }

  static warn(msg) {
    console.warn(msg);
  }

  static clear() {
    console.clear();
  }

}

export default Console;
